package bai_4;

public class LopHoc {
    String maLop;
    String tenMon;
    int soTinChi;
    String thoiGianHoc;
    String thoiGianBatDau;
    String thoiGianKetThuc;

    LopHoc(String maLop, String tenMon, int soTinChi, String thoiGianHoc, String thoiGianBatDau,
            String thoiGianKetThuc) {
        this.maLop = maLop;
        this.tenMon = tenMon;
        this.soTinChi = soTinChi;
        this.thoiGianHoc = thoiGianHoc;
        this.thoiGianBatDau = thoiGianBatDau;
        this.thoiGianKetThuc = thoiGianKetThuc;
    }
}
